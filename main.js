if(player=="Idly"){
	document.write("Idly");
}
else if(player=="Dosa"){
	document.write("Dosa");
}
else if(player=="Chapathi"){
	document.write("Chapathi");
}
else if(player="pongal"){
	document.write("pongal");
}
else{
	document.write("not your favorite food");
}